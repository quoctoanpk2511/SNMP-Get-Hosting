package com.demo.entity;

public class HrSWRunPerfTable {
	
	private int index;
	private String name;
	private String id;
	private String path;
	private String param;
	private String type;
	private String status;
	private String priority;
	private String perfCpu;
	private double perfMem;
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getParam() {
		return param;
	}
	public void setParam(String param) {
		this.param = param;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getPerfCpu() {
		return perfCpu;
	}
	public void setPerfCpu(String perfCpu) {
		this.perfCpu = perfCpu;
	}
	public double getPerfMem() {
		return perfMem;
	}
	public void setPerfMem(double perfMem) {
		this.perfMem = perfMem;
	}
	
}
